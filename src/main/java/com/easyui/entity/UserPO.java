package com.easyui.entity;


import java.io.Serializable;
import java.util.Date;

public class UserPO implements Serializable {

    private Long id;

    private String userName;

    private String nickName;

    private String password;

    private String createTimeStart;

    private String createTimeEnd;


    private String modifiedTimeStart;

    private String modifiedTimeEnd;

    private int page;

    private int rows;

    private String sort;

    private String order;

    private static final long serialVersionUID = 1L;

    /**
     * @return id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return user_name
     */
    public String getUserName() {
        return userName;
    }

    /**
     * @param userName
     */
    public void setUserName(String userName) {
        this.userName = userName;
    }

    /**
     * @return nick_name
     */
    public String getNickName() {
        return nickName;
    }

    /**
     * @param nickName
     */
    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    /**
     * @return password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password
     */
    public void setPassword(String password) {
        this.password = password;
    }

    public String getCreateTimeStart() {
        return createTimeStart;
    }

    public void setCreateTimeStart(String createTimeStart) {
        this.createTimeStart = createTimeStart;
    }

    public String getCreateTimeEnd() {
        return createTimeEnd;
    }

    public void setCreateTimeEnd(String createTimeEnd) {
        this.createTimeEnd = createTimeEnd;
    }

    public String getModifiedTimeStart() {
        return modifiedTimeStart;
    }

    public void setModifiedTimeStart(String modifiedTimeStart) {
        this.modifiedTimeStart = modifiedTimeStart;
    }

    public String getModifiedTimeEnd() {
        return modifiedTimeEnd;
    }

    public void setModifiedTimeEnd(String modifiedTimeEnd) {
        this.modifiedTimeEnd = modifiedTimeEnd;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getRows() {
        return rows;
    }

    public void setRows(int rows) {
        this.rows = rows;
    }

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getOrder() {
        return order;
    }

    public void setOrder(String order) {
        this.order = order;
    }
}
